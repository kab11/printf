#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	j;
	size_t	k;

	j = 0;
	k = 0;
	while (s1[k])
		k++;
	while (s2[j] && j < n)
	{
		s1[k] = ((char *)s2)[j];
		k++;
		j++;
	}
	s1[k] = '\0';
	return (s1);
}
